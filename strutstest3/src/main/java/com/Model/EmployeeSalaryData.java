package com.Model;

public class EmployeeSalaryData {
	private String emp_id,base_salary,expense_amount,leave_amount,curr_salary,curr_salary_date;

	public String getEmp_id() {
		return emp_id;
	}

	public void setEmp_id(String emp_id) {
		this.emp_id = emp_id;
	}

	public String getBase_salary() {
		return base_salary;
	}

	public void setBase_salary(String base_salary) {
		this.base_salary = base_salary;
	}

	public String getExpense_amount() {
		return expense_amount;
	}

	public void setExpense_amount(String expense_amount) {
		this.expense_amount = expense_amount;
	}

	public String getLeave_amount() {
		return leave_amount;
	}

	public void setLeave_amount(String leave_amount) {
		this.leave_amount = leave_amount;
	}

	public String getCurr_salary() {
		return curr_salary;
	}

	public void setCurr_salary(String curr_salary) {
		this.curr_salary = curr_salary;
	}

	public String getCurr_salary_date() {
		return curr_salary_date;
	}

	public void setCurr_salary_date(String curr_salary_date) {
		this.curr_salary_date = curr_salary_date;
	}
	
}
