package com.Model;

public class ExpenseData {
	private String expense_id,emp_id,event_date,claim_amount,reason,expense_status;

	public String getExpense_id() {
		return expense_id;
	}

	public void setExpense_id(String expense_id) {
		this.expense_id = expense_id;
	}

	public String getEmp_id() {
		return emp_id;
	}

	public void setEmp_id(String emp_id) {
		this.emp_id = emp_id;
	}

	public String getEvent_date() {
		return event_date;
	}

	public void setEvent_date(String event_date) {
		this.event_date = event_date;
	}

	public String getClaim_amount() {
		return claim_amount;
	}

	public void setClaim_amount(String claim_amount) {
		this.claim_amount = claim_amount;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getExpense_status() {
		return expense_status;
	}

	public void setExpense_status(String expense_status) {
		this.expense_status = expense_status;
	}
	
	
}
